def calibrate_offset(imu, samples=200):
    print("Calibrating… keep device still")
    s = 0
    for _ in range(samples):
        s += imu.get_values()[FORWARD_AXIS]
        sleep(0.01)
    offset = s / samples
    print("Calibration done, offset =", offset)
    return offset

# MAIN BRAKE DETECTOR

def brake_detector():
    # Calibration
    x_offset = calibrate_offset(imu)
    # Filter
    lpf = LowPass(LPF_ALPHA)
    # State:
    # 1 = not braking
    # 2 = slow braking
    # 3 = emergency braking
    state = 1
    print("Running brake detection...")
    while True:
        raw_x = imu.get_values()[FORWARD_AXIS]
        a = raw_x - x_offset
        a_f = lpf.update(a)

        # STATE MACHINE WITH HYSTERESIS
        if state == 1:
            if a_f < EMERG_ON:
                state = 3
            elif a_f < SLOW_BRAKE_ON:
                state = 2
        elif state == 2:
            if a_f < EMERG_ON:
                state = 3
            elif a_f > SLOW_BRAKE_OFF:
                state = 1
        elif state == 3:
            if a_f > EMERG_OFF:
                state = 2
        # Output (replace with LEDs, CAN, UART, etc.)
        if state == 1:
            print("1) Not braking      ", int(a_f))
        elif state == 2:
            print("2) Slow braking     ", int(a_f))
            brakelights()
        else:
            print("3) EMERGENCY BRAKE! ", int(a_f))
            brakelights()
            sync_to_thingsboard(nearaccident=True)
        sleep(SAMPLE_TIME)

