# Returnérer en værdi for solens status (og ja, man kunne lave det boolean, men synes at up/down-terminologien er mere læsbar, omend ikke helt så pythonic)
def get_where_the_sun_is(current_date):
    now = time.localtime()
    now_in_seconds_since_midnight = now[3] * 3600 + now[4] * 60 + now[5]
    sunrise_seconds_since_midnight = time_str_to_seconds(sunrise)
    sunset_seconds_since_midnight  = time_str_to_seconds(sunset)
    if sunrise_seconds_since_midnight <= now_in_seconds_since_midnight <= sunset_seconds_since_midnight:
        return "up"
    else:
        return "down"

